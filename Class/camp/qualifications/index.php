        <h1>Qualifications</h1>
        
<h2>Age</h2>
<p class="colophon">
We only accept Applicants that are in the age range of 13-19. Although we do 
ask that you have a group of at least 20 people to come. With that many kids 
of course you will need a chaperon so you are allowed to have 4 adults come to 
help watch the kids.
</p>
<h2>Grades</h2>
<p class="colophon">
We prefer students that have done excellent in a science class or any astronomy 
class. We look for students interested in space research and love of space. We 
would like their GPA to be above a 3.6 to even apply.
</p>
<h2>Essay</h2>
<p class="colophon">
Each applicant must send in an essay with the application. In this essay we 
would like you to write what you love so much about space and what you know 
about space. We also want to know a little bit about what you expect to learn 
from this program and why you want to participate in it.
</p>
<h2>Cost/Scholarship</h2>
<p class="colophon">
The cost of this camp is $4,000 for 8 weeks. You get a great education for 8 
weeks and you learn so much. This is a jump start on a career working for Nasa. 
We encourage all students to do a fundraiser and try and raise this money or 
look for scholarships to make this camp possible. This is a great responsibility
for your child.
</p>
<h2>Recommendation Letter</h2>
<p class="colophon">
Each student must have a recommendation letter from a teacher or professor to 
tell why they think this student deserves to come to space camp.
</p>                   
